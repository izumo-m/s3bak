def iter_tree(root: str, excludes: list[str]) -> Iterator[tuple[str, os.stat_result, str | None]]:
    """Walk directory tree yielding (rel, lstat_result, symlink_target|None).

    rel uses "./" prefix (e.g., ".", "./sub/file").
    """
    prune_patterns, skip_patterns = _split_excludes(excludes)

    st = os.lstat(root)
    yield ".", st, None

    for dirpath, dirnames, filenames in os.walk(root, followlinks=False):
        rel_dir = os.path.relpath(dirpath, root)
        rel_prefix = "./" if rel_dir == "." else f"./{rel_dir}/"

        dirnames.sort()
        filenames.sort()

        to_remove: list[str] = []
        for d in dirnames:
            rel = f"{rel_prefix}{d}"
            if any(path_match(rel, p) for p in prune_patterns):
                to_remove.append(d)
                continue
            full = os.path.join(dirpath, d)
            st = os.lstat(full)
            if stat_mod.S_ISLNK(st.st_mode):
                yield rel, st, os.readlink(full)
                to_remove.append(d)
            else:
                yield rel, st, None
        for d in to_remove:
            dirnames.remove(d)

        for f in filenames:
            rel = f"{rel_prefix}{f}"
            if any(path_match(rel, p) for p in skip_patterns):
                continue
            full = os.path.join(dirpath, f)
            st = os.lstat(full)
            if stat_mod.S_ISLNK(st.st_mode):
                yield rel, st, os.readlink(full)
            else:
                yield rel, st, None


def iter_local_tree(outpath: str, excludes: list[str]) -> Iterator[tuple[str, bool]]:
    """Walk local tree yielding (rel_without_dot_slash, is_dir)."""
    prune_patterns, skip_patterns = _split_excludes(excludes)

    for dirpath, dirnames, filenames in os.walk(outpath, followlinks=False):
        rel_dir = os.path.relpath(dirpath, outpath)
        rel_prefix = "./" if rel_dir == "." else f"./{rel_dir}/"

        dirnames.sort()
        filenames.sort()

        to_remove: list[str] = []
        for d in dirnames:
            rel = f"{rel_prefix}{d}"
            if any(path_match(rel, p) for p in prune_patterns):
                to_remove.append(d)
                continue
            yield rel[2:], True  # strip "./"
        for d in to_remove:
            dirnames.remove(d)

        for f in filenames:
            rel = f"{rel_prefix}{f}"
            if any(path_match(rel, p) for p in skip_patterns):
                continue
            yield rel[2:], False


