@dataclass(frozen=True)
class ManifestEntry:
    rel: str  # original name field: ".", "./foo/bar", or bare basename
    mode_str: str
    user: str
    group: str
    size_str: str
    date_str: str
    time_str: str
    zone_str: str
    sym_target: str | None

    @property
    def mode_int(self) -> int:
        return int(self.mode_str, 8)

    @property
    def size_int(self) -> int:
        return int(self.size_str)

    @property
    def epoch(self) -> int:
        return int(parse_timestamp(self.date_str, self.time_str, self.zone_str))


def parse_manifest_line(line: str) -> ManifestEntry | None:
    line = line.rstrip("\n")
    if not line:
        return None
    parts = line.split(None, 7)
    if len(parts) < 8:
        return None
    fname, sym = parse_fname(parts[7])
    return ManifestEntry(
        rel=fname,
        mode_str=parts[0],
        user=parts[1],
        group=parts[2],
        size_str=parts[3],
        date_str=parts[4],
        time_str=parts[5],
        zone_str=parts[6],
        sym_target=sym,
    )


def iter_manifest(manifest_path: str) -> Iterator[ManifestEntry]:
    with open(manifest_path) as f:
        for line in f:
            entry = parse_manifest_line(line)
            if entry is not None:
                yield entry


def resolve_manifest_rel(rel_field: str, sub: str | None) -> str | None:
    """Translate manifest rel ('.' / './x/y' / 'basename') into the
    sub-relative form ('.' for self, 'x/y' for descendants, None to skip).
    """
    rel = rel_field.removeprefix("./")
    if rel_field == ".":
        rel = "."
    if sub is None:
        return rel
    if rel == sub:
        return "."
    if rel.startswith(sub + "/"):
        return rel[len(sub) + 1 :]
    return None


def manifest_target(
    entry: ManifestEntry, outpath: str, is_dir: bool, sub: str | None
) -> tuple[str, str] | None:
    """Resolve the manifest entry to (target_path, sub_rel) or None to skip."""
    rel = resolve_manifest_rel(entry.rel, sub)
    if rel is None:
        return None
    if is_dir:
        target = outpath if rel == "." else os.path.join(outpath, rel)
    else:
        target = outpath
    return target, rel


