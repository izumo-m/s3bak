def _windows_collect_writable_prep(
    outpath: str, is_dir: bool, manifest_path: str, sub: str | None
) -> list[tuple[str, int]]:
    # Windows only. Walk the manifest, find existing local files that are:
    #   - regular files (not dir / not symlink)
    #   - read-only (owner write bit clear)
    #   - differ from the manifest in size or mtime
    # Temporarily add owner-write so `boto3-s3 sync`/`cp` can overwrite them.
    # Returns [(path, original_mode), ...] for later restoration.
    targets: list[tuple[str, int]] = []
    try:
        for entry in iter_manifest(manifest_path):
            if entry.sym_target is not None:
                continue
            res = manifest_target(entry, outpath, is_dir, sub)
            if res is None:
                continue
            target, _rel = res
            try:
                st = os.lstat(target)
            except OSError:
                continue
            if not stat_mod.S_ISREG(st.st_mode):
                continue
            if st.st_mode & stat_mod.S_IWRITE:
                continue
            try:
                size = entry.size_int
            except ValueError:
                size = -1
            differs = size < 0 or st.st_size != size
            if not differs:
                try:
                    differs = int(st.st_mtime) != entry.epoch
                except (ValueError, OSError):
                    differs = True
            if not differs:
                continue
            try:
                os.chmod(target, st.st_mode | stat_mod.S_IWRITE)
            except OSError:
                continue
            targets.append((target, st.st_mode))
    except OSError:
        pass
    return targets


def _windows_restore_modes(targets: list[tuple[str, int]]) -> None:
    for target, original_mode in targets:
        try:
            os.chmod(target, original_mode)
        except OSError:
            continue


def _apply_meta(target: str, mode: int, epoch: float) -> bool:
    ok = True
    try:
        os.utime(target, (epoch, epoch))
    except PermissionError as e:
        err(f"utime failed (not owner?): {target}: {e}")
        ok = False
    try:
        os.chmod(target, mode)
    except PermissionError as e:
        err(f"chmod failed (not owner?): {target}: {e}")
        ok = False
    return ok


def apply_manifest(
    cfg: Config,
    entry: str,
    outpath: str,
    is_dir: bool,
    manifest_path: str,
    sub: str | None = None,
    verbose: bool = False,
) -> int:
    remote_keys: set[str] = set()
    if is_dir:
        assert cfg.store is not None
        all_keys = cfg.store.list_keys_under(entry, verbose=verbose)
        if sub is not None:
            sub_prefix = sub + "/"
            for k in all_keys:
                if k.startswith(sub_prefix):
                    remote_keys.add(k[len(sub_prefix) :])
        else:
            remote_keys = all_keys

    deferred_dirs: list[tuple[str, int, float]] = []
    errors = 0

    for m_entry in iter_manifest(manifest_path):
        res = manifest_target(m_entry, outpath, is_dir, sub)
        if res is None:
            continue
        target, rel = res
        epoch = parse_timestamp(m_entry.date_str, m_entry.time_str, m_entry.zone_str)
        mode = m_entry.mode_int

        if m_entry.sym_target is not None:
            parent = os.path.dirname(target)
            if parent:
                os.makedirs(parent, exist_ok=True)
            # Clear whatever is already there. islink first, so a symlink is
            # removed as a link (never recursing into its target); a real dir
            # (e.g. left by an older follow-symlinks backup) is removed wholesale.
            if os.path.islink(target) or os.path.isfile(target):
                os.remove(target)
            elif os.path.isdir(target):
                shutil.rmtree(target)
            os.symlink(m_entry.sym_target, target)
            write_output(f"{target} -> {m_entry.sym_target}\n")
            continue

        if not is_dir:
            kind = "file"
        elif rel == ".":
            kind = "dir"
        elif rel in remote_keys:
            kind = "file"
        else:
            kind = "dir"

        if kind == "dir":
            if not os.path.exists(target):
                os.makedirs(target, exist_ok=True)
            deferred_dirs.append((target, mode, epoch))
            continue

        if not os.path.exists(target):
            err(f"expected file missing (sync did not place it): {target}")
            errors += 1
            continue

        write_output(f"{m_entry.mode_str} {target}\n")
        if not _apply_meta(target, mode, epoch):
            errors += 1

    deferred_dirs.sort(key=lambda x: x[0], reverse=True)
    for target, mode, epoch in deferred_dirs:
        write_output(f"{format(mode, 'o')} {target}\n")
        if not _apply_meta(target, mode, epoch):
            errors += 1

    return 1 if errors else 0


