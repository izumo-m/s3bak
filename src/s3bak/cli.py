def format_stat_line(rel: str, st: os.stat_result, sym_target: str | None) -> str:
    """Format like: stat -c '%a %U %G %s %y %N' with QUOTING_STYLE=shell-always."""
    mode = format(stat_mod.S_IMODE(st.st_mode), "o")

    if pwd is not None:
        try:
            owner = pwd.getpwuid(st.st_uid).pw_name
        except KeyError:
            owner = str(st.st_uid)
    else:
        owner = str(st.st_uid)
    if grp is not None:
        try:
            group = grp.getgrgid(st.st_gid).gr_name
        except KeyError:
            group = str(st.st_gid)
    else:
        group = str(st.st_gid)

    size = st.st_size

    epoch_sec = st.st_mtime_ns // 1_000_000_000
    nsec = st.st_mtime_ns % 1_000_000_000
    dt = datetime.datetime.fromtimestamp(epoch_sec, tz=datetime.timezone.utc).astimezone()
    utc_off = dt.utcoffset()
    assert utc_off is not None
    total_sec = int(utc_off.total_seconds())
    sign = "+" if total_sec >= 0 else "-"
    total_sec = abs(total_sec)
    h, rem = divmod(total_sec, 3600)
    m = rem // 60
    mtime_str = f"{dt.strftime('%Y-%m-%d %H:%M:%S')}.{nsec:09d} {sign}{h:02d}{m:02d}"

    if sym_target is not None:
        name_field = f"{shell_always_quote(rel)} -> {shell_always_quote(sym_target)}"
    else:
        name_field = shell_always_quote(rel)

    return f"{mode} {owner} {group} {size} {mtime_str} {name_field}"


def write_manifest_to_aws(
    cfg: Config, entry: str, target: str, excludes: list[str], verbose: bool
) -> None:
    entrylist = f"{entry}-ls-l.txt"
    write_stderr(f"Updating {cfg.prefix}/{entrylist}\n")

    lines: list[str] = []
    if os.path.isdir(target):
        for rel, st, sym_target in iter_tree(target, excludes):
            lines.append(format_stat_line(rel, st, sym_target))
    else:
        basename = os.path.basename(target)
        st = os.lstat(target)
        sym = os.readlink(target) if stat_mod.S_ISLNK(st.st_mode) else None
        lines.append(format_stat_line(basename, st, sym))

    manifest_data = "\n".join(lines) + "\n" if lines else ""
    assert cfg.store is not None
    cfg.store.put_text(entrylist, manifest_data, verbose=verbose)


def upload_manifest(cfg: Config, entry: str, target: str, excludes: list[str], opts: Opts) -> int:
    post_hook: str | None = cfg.entries[entry].get("post_hook")

    if opts.dryrun:
        print(f"(dryrun) would update manifest: {entry}-ls-l.txt")
        if post_hook:
            print(f"(dryrun) would run post_hook: {post_hook}")
        return 0

    write_manifest_to_aws(cfg, entry, target, excludes, opts.verbose)

    if post_hook:
        if opts.verbose:
            write_stderr(f"+ {post_hook}\n")
        subprocess.run(["bash", "-c", post_hook])

    return 0


def _manifest_line_key(line: str) -> str:
    entry = parse_manifest_line(line)
    return "" if entry is None else entry.rel.removeprefix("./")


def _iter_sub_tree_lines(local_sub: str, sub: str, excludes: list[str]) -> Iterator[str]:
    st = os.lstat(local_sub)
    if stat_mod.S_ISLNK(st.st_mode):
        sym = os.readlink(local_sub)
        yield format_stat_line(f"./{sub}", st, sym)
        return
    if not os.path.isdir(local_sub):
        yield format_stat_line(f"./{sub}", st, None)
        return
    for rel, st2, sym_target in iter_tree(local_sub, excludes):
        if rel == ".":
            disp = f"./{sub}"
        else:
            tail = rel[2:]
            disp = f"./{sub}/{tail}"
        yield format_stat_line(disp, st2, sym_target)


def patch_manifest_subtree(
    cfg: Config,
    entry: str,
    target_root: str,
    sub: str,
    excludes: list[str],
    opts: Opts,
) -> None:
    """Download the manifest, replace the lines under `sub`, and re-upload it.

    target_root/sub may be a file, a symlink, or a directory.
    If it does not exist, the lines under that `sub` are simply removed.
    """
    entrylist = f"{entry}-ls-l.txt"
    if opts.dryrun:
        print(f"(dryrun) would patch manifest: {entrylist} (sub={sub})")
        return

    fd, manifest_path = tempfile.mkstemp(suffix=".txt")
    os.close(fd)
    try:
        existing: list[str] = []
        if download_manifest(cfg, entry, manifest_path, opts.verbose):
            with open(manifest_path) as f:
                for line in f:
                    line = line.rstrip("\n")
                    if line:
                        existing.append(line)

        kept: list[str] = []
        for line in existing:
            entry_obj = parse_manifest_line(line)
            if entry_obj is None:
                kept.append(line)
                continue
            rel = entry_obj.rel.removeprefix("./")
            if rel == sub or rel.startswith(sub + "/"):
                continue
            kept.append(line)

        new_lines: list[str] = []
        local_sub = os.path.join(target_root, sub)
        if os.path.lexists(local_sub):
            new_lines = list(_iter_sub_tree_lines(local_sub, sub, excludes))

        merged = kept + new_lines
        merged.sort(key=_manifest_line_key)

        data = "\n".join(merged) + "\n" if merged else ""
        write_stderr(f"Updating {cfg.prefix}/{entrylist}\n")
        assert cfg.store is not None
        cfg.store.put_text(entrylist, data, verbose=opts.verbose)
    finally:
        os.unlink(manifest_path)


