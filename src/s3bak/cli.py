_output_lock = threading.Lock()


def err(msg: str) -> None:
    sys.stderr.write(f"{PROG}: {msg}\n")
    sys.stderr.flush()


def die(msg: str) -> NoReturn:
    err(msg)
    sys.exit(1)


def write_output(text: str) -> None:
    with _output_lock:
        sys.stdout.write(text)
        sys.stdout.flush()


def write_stderr(text: str) -> None:
    with _output_lock:
        sys.stderr.write(text)
        sys.stderr.flush()


def echo_command(verbose: bool, args: list[str]) -> None:
    if verbose:
        write_stderr(f"+ {shlex.join(args)}\n")


def expand_home(path: str) -> str:
    # os.path.expanduser on Windows-native Python (ucrt64/mingw) resolves "~"
    # via USERPROFILE, which ignores the msys HOME. Prefer HOME when set.
    if not path.startswith("~"):
        return path
    home = os.environ.get("HOME")
    if home and (path == "~" or path.startswith("~/")):
        return home + path[1:]
    return os.path.expanduser(path)


# =============================================================================
# Shell quoting (stat QUOTING_STYLE=shell-always compatible)
# =============================================================================


def shell_always_quote(s: str) -> str:
    return "'" + s.replace("'", "'\\''") + "'"


def shell_unquote(s: str) -> str:
    if s.startswith("'"):
        s = s[1:-1]
        s = s.replace("'\\'", "\x00").replace("'", "")
        s = s.replace("\x00", "'")
        return s
    if s.startswith('"'):
        return s[1:-1]
    return s


def parse_fname(fname_field: str) -> tuple[str, str | None]:
    m = re.match(r"^(.+?)' -> '(.+)$", fname_field)
    if m:
        link_part = m.group(1) + "'"
        target_part = "'" + m.group(2)
        return shell_unquote(link_part), shell_unquote(target_part)
    return shell_unquote(fname_field), None


def parse_timestamp(date_str: str, time_str: str, zone_str: str) -> float:
    # Expected formats (generated by format_stat_line):
    #   date_str: "YYYY-MM-DD"
    #   time_str: "HH:MM:SS" or "HH:MM:SS.nnnnnnnnn"
    #   zone_str: "+HHMM" or "-HHMM"
    # Fast path avoids datetime.strptime, which dominates manifest parsing.
    try:
        year = int(date_str[0:4])
        month = int(date_str[5:7])
        day = int(date_str[8:10])
        hour = int(time_str[0:2])
        minute = int(time_str[3:5])
        second = int(time_str[6:8])
        if zone_str[0] == "+":
            sign = 1
        elif zone_str[0] == "-":
            sign = -1
        else:
            raise ValueError(f"bad zone: {zone_str}")
        tz_h = int(zone_str[1:3])
        tz_m = int(zone_str[3:5])
    except (ValueError, IndexError):
        # Fall back to strptime for any unexpected shape.
        time_fb = time_str.split(".")[0]
        dt = datetime.datetime.strptime(f"{date_str} {time_fb} {zone_str}", "%Y-%m-%d %H:%M:%S %z")
        return dt.timestamp()
    utc_epoch = calendar.timegm((year, month, day, hour, minute, second, 0, 0, 0))
    return float(utc_epoch - sign * (tz_h * 3600 + tz_m * 60))


